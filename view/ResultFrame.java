    private void addTableValue(){
        ArrayList<String> companies = new ArrayList<>();
        try{
            ResultSet rs = dbc.executeQuery("SELECT " + search + "Name FROM " + search + " ORDER BY " + search + "ID");
            String profitResult;
            while(rs.next()){
                companies.add(rs.getString(search + "Name"));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        String[][] values = new String[companies.size()][13];
        
        for(int i = 0 ; i < companies.size() ; i ++){
            for(int j = 0 ; j < 13 ; j++){
                values[i][j] = "0";
            }
        }
        for(int i = 0 ; i < companies.size() ; i ++){
            values[i][0] = companies.get(i);
            addValues(values, i);
        }
        this.values = values;
        
    }
