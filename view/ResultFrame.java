    private void addValues(String[][] val, int cId){
        int year;
        try{
            year = (Integer) yearSpinner.getModel().getValue(); 
        }catch(Exception e){
            year = 2017;
        }
        
        try{
            String profitResult = "SELECT MONTH(o.orderdate) as 'month', SUM(itemPrice) as 'total'\n" +
                                    "FROM order_details od, "+ search +" c, `order` o, items i\n" +
                                    "WHERE od.orderid = o.orderid AND\n" +
                                    "	od.itemID = i.itemID AND\n" +
                                    "    i." + search + "ID = c." + search + "ID AND\n" +
                                    "    YEAR(o.orderdate) = '"+ year +"' AND\n" +
                                    "    c." + search + "ID = '" + (cId + 1) + "' " +
                                    "GROUP BY MONTH(o.orderdate)";
            ResultSet rs = dbc.executeQuery(profitResult);
            int index;
            String value;
            while(rs.next()){
                index = rs.getInt("month");
                value = rs.getString("total");
                val[cId][index] = value;
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
